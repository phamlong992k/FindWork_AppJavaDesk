    private void myInit()
    {
        UserDAO dao = new UserDAO();
        Vector<String> list = new Vector<>();
        try {
            list = dao.getJobData();
            ArrayList<Worker> workList = new ArrayList();
            workList = dao.getWorker(idUser);
            for (Worker worker : workList) 
            {
                String name = dao.getJobName(worker.getIdJob());
                cbxCandidateJob.addItem(name);
                jTextPane1.setText(worker.getDetail());
            }
        } catch(Exception e){
            e.printStackTrace();
        }
        listJob.setListData(list);
    }

