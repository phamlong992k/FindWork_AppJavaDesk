    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String value = "";
        value = listJob.getSelectedValue();
        boolean check = true;
        if(!value.equals(""))
        {
            int num = cbxCandidateJob.getItemCount();
            for(int i = 0; i < num; i ++)
            {
                if(cbxCandidateJob.getItemAt(i).equals(value))
                    check = false;
            }
            if(check == true)
                cbxCandidateJob.addItem(value);
        }
        else
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để thêm vào danh sách tìm kiếm");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int index = cbxCandidateJob.getSelectedIndex();
        String value = cbxCandidateJob.getItemAt(index);
        UserDAO dao = new UserDAO();
        try {
            int idJob = dao.getIdOfJob(value);
            if(idJob >= 0)
            {
                boolean check = dao.checkExistWorker(idUser, idJob);
                if(check == true)
                    dao.removeWorker(idUser, idJob);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
        cbxCandidateJob.removeItemAt(index);
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnDetailUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailUploadActionPerformed
        // TODO add your handling code here:
        String value = (String) cbxCandidateJob.getSelectedItem();
        UserDAO dao = new UserDAO();
        WorkerDAO workerDAO = new WorkerDAO();
        String detail = jTextPane1.getText();        
        try {
            int idJob = dao.getIdOfJob(value);
            boolean check = workerDAO.findWorker(idUser, idJob);
            if(check == false)
            {
                Worker worker = new Worker(idUser, idJob, detail);
                dao.addWorker(worker);
                JOptionPane.showMessageDialog(this, "Đăng tin thành công");
            }
            else
            {
                workerDAO.updateDetailWorker(idUser, idJob, detail);
                JOptionPane.showMessageDialog(this, "Sửa tin thahf công");
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Thêm tin thất bại");
            Logger.getLogger(DetailCandidate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDetailUploadActionPerformed

