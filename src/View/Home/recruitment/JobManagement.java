    public void setTable(User user){
       listRequirementJobs=requireJobDAO.findRequireJobByIdUser(user.getIdUser());
       JobDAO jobDAO= new JobDAO();
       
       DefaultTableModel defaultTableModel= (DefaultTableModel) tblJopManage.getModel();
       Vector data= new Vector();
        
       for(RequirementJob re:listRequirementJobs){
           System.out.println(re);
           Vector row= new Vector();
           row.add(jobDAO.getJobById(re.getIdJob()).getName());
           row.add(re.getTitle());
           if(re.getStatus()==1){
               row.add("Đang tìm kiếm");
           }
           else{
               row.add("Đã hủy");
           }
           row.add(re.getProvince());
           row.add(re.getRating());
           data.add(row);
       }
       
       defaultTableModel.setDataVector(data,header);
       tblJopManage.setModel(defaultTableModel);
    }
