    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if(tblJopManage.getSelectedRowCount()==1&&!listRequirementJobs.isEmpty()){
            int i=tblJopManage.getSelectedRow();
          
            int idJob=listRequirementJobs.get(i).getIdJob();
            
            
            int check=requireJobDAO.deleteRequirementJobs(idJob);
            if(check>0){
                check=ratingDAO.deleteRating(idJob,user.getIdUser());
                if(check>0){
                    JOptionPane.showMessageDialog(null,"Remove Success ");
                    setTable(user);
            
                }
                else{
                    JOptionPane.showMessageDialog(null,"Remove Fail rating");
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Remove Fail job");
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


